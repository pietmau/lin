package com.lisnx.model;

public class UserConnectivity {
	private String facebookConnectivity;
	private String linkedInConnectivity;
	private String twitterConnectivity;
	
	public String getFacebookConnectivity() {
		return facebookConnectivity;
	}
	public void setFacebookConnectivity(String facebookConnectivity) {
		this.facebookConnectivity = facebookConnectivity;
	}
	public String getLinkedInConnectivity() {
		return linkedInConnectivity;
	}
	public void setLinkedInConnectivity(String linkedInConnectivity) {
		this.linkedInConnectivity = linkedInConnectivity;
	}
	public String getTwitterConnectivity() {
		return twitterConnectivity;
	}
	public void setTwitterConnectivity(String twitterConnectivity) {
		this.twitterConnectivity = twitterConnectivity;
	}
}