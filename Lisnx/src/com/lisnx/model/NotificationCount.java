package com.lisnx.model;

public class NotificationCount {
	private int peopleNearByCount;
	private int friendRequestCount;
	
	public int getPeopleNearByCount() {
		return peopleNearByCount;
	}
	public void setPeopleNearByCount(int peopleNearByCount) {
		this.peopleNearByCount = peopleNearByCount;
	}
	public int getFriendRequestCount() {
		return friendRequestCount;
	}
	public void setFriendRequestCount(int friendRequestCount) {
		this.friendRequestCount = friendRequestCount;
	}
}